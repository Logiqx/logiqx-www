<h1>1 March 2004</h1>
<table class="news">
  <tr>
    <td>
      <p>I am back from a weekend away and found that the first
      emulator has been released that supports the new Neo-Geo re-dumps.</p>
      <p>Since it was such a simple matter for me to provide a
      tool that can build them for you, here it is:</p>
      <p><a href="Tools/ROMBuild/rbmame80.zip"> ROMBuild [MAME v0.80 special]</a>
      (yes, it was named in advance of any emulator releases)</p>
      <p>29 games have been re-dumped but 3 of the games only
      have changes in the parent.</p>
      <p>This tool will create ROMs for all 29 games, although in
      a split-merged ROM collection it is only the 26 parent ZIPs that must
      change (the number of games that the tool says it supports).
    </td>
  </tr>
</table>

