<h1>25 January 2003</h1>
<table class="news">
  <tr>
    <td>
      <p>I have finally found time to update to my <a href="HowTo/HowTo.php">compile
      guides</a> for MAME and RAINE!</p>
      <p>These guides have been written for the level of
      understanding that I like to have myself.<br />
      If you want to understand a little more about what is being used by MAME
      and RAINE then you may find them useful.<br />
      If you want don't care what everything is and don't want to compile RAINE
      then just use the downloads at the MAME page. ;)</p>
      <p>The biggest changes in this update are:</p>
          <p>1. Both the DJGPP and
          <a href="HowTo/WinMAME.php"> MinGW</a> environments use GCC 3
          (previously they used GCC 2).<br />
          2. You can create a single <a href="HowTo/WinMAME.php"> MinGW</a>
          environment that works
          with MAME and RAINE32 (no more DirectX conflicts).<br />
          3. The DJGPP environment now works under NT and XP (no
          more 'Out of DOS memory', 'No DPMI selectors', etc).
    </td>
  </tr>
</table>

