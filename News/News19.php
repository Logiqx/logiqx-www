<h1>4 December 2004</h1>
<table class="news">
  <tr>
    <td>
        <p>I have updated my guides about <a href="HowTo/HowTo.php">compiling
        MAME and RAINE</a> again!</p>
        <p>I have now split the guide into two parts since a lot
        of stuff is RAINE specific and not needed by MAME.</p>
        <p><b>The following changes have been made to the MAME
        guide:</b></p>
        <ul>
          <li>
            <p>Updated the GNU tools to more recent versions
            (taken from <a href="http://gnuwin32.sourceforge.net/">http://gnuwin32.sourceforge.net/</a>)</li>
          <li>
            <p>Explained how to build a Pentium Pro optimised
            version.</li>
          <li>
            <p>Explained when 'make clean' should be used.</li>
          <li>
            <p>Explained how to use apply MAME updates using
            'patch' (for building interim releases).</li>
          <li>
            <p>Removed the the UNIX_TOOLS variable (since it is
            no longer required).</li>
        </ul>
        <p><b>The following changes have been made to the RAINE32
        guide:</b></p>
        <ul>
          <li>
            <p>Added a section about MSYS because it is required
            to compile SDL and RAINE.</li>
          <li>
            <p>Added a section about SDL because it is used by
            RAINE v0.40.3 onwards.</li>
          <li>
            <p>Removed the section on SEAL because it is no
            longer used (SDL is used instead).</li>
          <li>
            <p>Updated Allegro to the stable 4.0.3 release.</li>
          <li>
            <p>Updated zlib to version 1.2.1.</li>
          <li>
            <p>Updated UPX to version 1.25.</li>
        </ul>
        <p>There is still some work to be done on the RAINE guide
        but is now better than it was!
    </td>
  </tr>
</table>

